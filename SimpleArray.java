public class SimpleArray {
    public static void main(String[] args) {
        // Step 1: Create an array
        int[] numbers = new int[3]; // 3 boxes
        
        // Step 2: Add values
        numbers[0] = 5;   // First box
        numbers[1] = 10;  // Second box
        numbers[2] = 15;  // Third box
        
        // Step 3: Print values
        System.out.println(numbers[0]); // Prints 5
        System.out.println(numbers[1]); // Prints 10
        System.out.println(numbers[2]); // Prints 15 
    }
}
